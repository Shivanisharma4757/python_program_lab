# Basic salary of the employee
basic_salary = 25000

# Calculate HRA as 20% of basic salary
# 20% means (20 / 100) which equals 0.2
hra = basic_salary * (20 / 100)

# Bonus amount
bonus = 5000

# Calculate total monthly salary by adding basic salary, HRA, and bonus
total_salary = basic_salary + hra + bonus

# Display the results
print("HRA:", hra)

print("Total monthly salary:", total_salary)

# output
# HRA: 5000.0
# Total monthly salary: 35000.0
