# Program to multiply two two-digit numbers

# Take first two-digit number as input
num1 = int(input("Enter the first two-digit number: "))

# Take second two-digit number as input
num2 = int(input("Enter the second two-digit number: "))

# Calculate the product
product = num1 * num2

# Display the result
print("The multiplication of", num1, "and", num2, "is:", product)