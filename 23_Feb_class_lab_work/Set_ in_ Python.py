# ------------------------------------------
# Program to demonstrate Set in Python
# ------------------------------------------

# Creating a set with repeated elements
set1 = {45, 67, 89, 45, 67, 89, 34, 90, 45, 59, 120, 34, 90, 45, 59, 120}

# Displaying the set
print("Set is:")
print(set1)

# ------------------------------------------
# Inserting an element into the set
# ------------------------------------------

# Adding 34 to the set
set1.add(34)

print("After inserting 34 into the set:")
print(set1)

#output
#Set is:
#{34, 67, 89, 45, 90, 59, 120}
#After inserting 34 into the set:
#{34, 67, 89, 45, 90, 59, 120}
