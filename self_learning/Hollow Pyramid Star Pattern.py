# Number of rows
n = 5

for i in range(1, n + 1):
    # Print leading spaces
    print(' ' * (n - i), end='')
    
    # Print stars and spaces
    for j in range(1, 2 * i):
        if j == 1 or j == 2 * i - 1 or i == n:
            print('*', end='')
        else:
            print(' ', end='')
    
    print()  # Move to next line

